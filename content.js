// ===== Derived Maps =====
let reverseMapCache = null;

function buildReverseMap(layout) {
	if (reverseMapCache) return reverseMapCache;

	const map = {};

	for (const code in layout) {
		const entry = layout[code];

		for (const variant of ["en", "enShift", "ru", "ruShift"]) {
			const value = entry[variant];
			if (!value) continue;

			if (!map[value]) {
				map[value] = new Set();
			} 
			map[value].add({
				code,
				lang: variant.startsWith("en") ? "en" : "ru",
				shift: variant.includes("Shift")
			});
			
		}
	}

	return reverseMapCache = map;
}

// ===== Conversion Engine =====
const exceptionHandlers = [
	function twitchHandler(el) {
		const editor = el?.closest('[data-a-target="chat-input"]');
		if (!editor) return false;

		const selection = document.getSelection();
		const range = document.createRange();

		const textNode = editor.querySelector('[data-slate-string="true"]')?.firstChild;
		const convertedText = convertLayout(textNode?.textContent || editor.textContent);
		
		const dataTransfer = new DataTransfer();
		dataTransfer.setData('text/plain', convertedText);
		

		if (textNode) {
			range.setStart(textNode, 0);
			range.setEnd(textNode, textNode.textContent.length);
		} else {
			range.selectNodeContents(editor);
		}

		selection.removeAllRanges();
		selection.addRange(range);

		setTimeout(() => {
			editor.dispatchEvent(new ClipboardEvent('paste', {
				clipboardData: dataTransfer,
				bubbles: true,
				cancelable: true
			}));
		}, 80);
		
		return true;
	},
	function defaultHandler(el) {
		const selection = document.getSelection();

		const node = getTextNodeAtCursor(selection);
		if(!node) return false;

		if (selection.isCollapsed) {
			node.textContent = convertLayout(node.textContent);
			return true;
		}

		const range = selection.getRangeAt(0);
		const textNode = document.createTextNode(convertLayout(selection.toString()));

		range.deleteContents();
		range.insertNode(textNode);

		return true;
	}
];

function processContentEditable(el) {
	for (const handler of exceptionHandlers) 
		if (handler(el)) return;
}

function convertInput(el) {
	const start = el.selectionStart;
	const end = el.selectionEnd;

	if (start !== end) {
		const selected = el.value.slice(start, end);
		el.setRangeText(convertLayout(selected), start, end, "select");
	} else {
		el.value = convertLayout(el.value);
	}
}

function convertLastWord() {
	const el = document.activeElement;
	if (!el) return;

	if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
		const value = el.value.toString();
		const cursorPos = el.selectionStart;
		const wordStart = findWordStart(value, cursorPos);

		const lastWord = value.slice(wordStart, cursorPos - 1);
		const convertedWord = convertLayout(lastWord);

		if (!check(normalize(convertedWord), detectLayout(convertedWord)) ||
			check(normalize(lastWord), detectLayout(lastWord))) return;

		const newCursor = wordStart + convertedWord.length + 1;

		el.value = value.slice(0, wordStart) + convertedWord + value.slice(cursorPos - 1);
		el.setSelectionRange(newCursor, newCursor);
		
	} 
	else if (isContentEditable(el)) 	{
		const selection = window.getSelection();

		const node = getTextNodeAtCursor(selection);
		if(!node) return;
		
		const range = selection.getRangeAt(0);
		const text = node.textContent;
		
		const cursorPos = range.startOffset - 1;
		const wordStart = findWordStart(text, cursorPos);

		const lastWord = text.slice(wordStart, cursorPos);
		const convertedWord = convertLayout(lastWord);

		if (!check(normalize(convertedWord), detectLayout(convertedWord)) ||
			check(normalize(lastWord), detectLayout(lastWord))) return;

		node.textContent = text.slice(0, wordStart) +
			convertedWord +
			text.slice(cursorPos);

		const newRange = document.createRange();

		newRange.setStart(node, cursorPos + 1);
		newRange.collapse(false);

		selection.removeAllRanges();
		selection.addRange(newRange); 
	}
	
}

function convertLayout(str) {
	const target = detectLayout(str) === "en" ? "ru" : "en";

	return str.split("").map(ch => {
		const infos = reverseMap[ch];
		if (!infos) return ch;
		
		for (const info of infos.values()) {
			if (info.lang === target) continue;

			const key = codeLayout[info.code];

			if (info.shift && key[target + "Shift"]) 
				return key[target + "Shift"];
			else
				return key[target] || ch; 
		}
		return ch;
	}).join("");
}

// ===== Utils =====
function check(word, lang) {
	return DICTS[lang].has(word);
}

function normalize(word) {
	return word
		.toLowerCase()
		.normalize("NFC")
		.trim();
}

function findWordStart(str, cursor) {
	let start = cursor - 1;
	if (start < 0) return 0;

	while (start > 0 && !separators.includes(str[start - 1])) {
		start--;
	}
	return start;
}

function detectLayout(str) {
	let enCount = 0;
	let ruCount = 0;

	for (const ch of str) {
		const infos = reverseMap[ch];
		
		if (!infos || infos.size > 1) continue;

		const [lang] = infos;

		if (lang.lang === "en") enCount++;
		if (lang.lang === "ru") ruCount++;
	}

	return (ruCount > enCount)? "ru" : "en"; 
}

function getTextNodeAtCursor(selection) {
	let node = selection.anchorNode;

	while (node && node.nodeType !== Node.TEXT_NODE) {
		node = node.childNodes[selection.anchorOffset] || node.firstChild;
	}

	return node;
}

function isContentEditable(el) {
	return el?.getAttribute?.("contenteditable");
}

function enableAuto(event) {
	if (separators.includes(event.key)) {
		clearTimeout(timer);
		timer = setTimeout(convertLastWord, 300);
	}
}

// ===== Сontrollers =====
const reverseMap = buildReverseMap(codeLayout);
const separators = [" ", "Enter", ".", ",", "/", "?", "-", "!", "&", "'"];
const activeTabUrl = window.location.origin;

let timer;

chrome.runtime.onMessage.addListener((msg) => {
	if (msg?.type === "convert") {
		const el = document.activeElement;
		if (!el) return;

		if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") 
			convertInput(el);
		else if (isContentEditable(el)) processContentEditable(el);

		return;
	}

	if (msg?.type === "update") {
		document.removeEventListener("keyup", enableAuto);

		if (msg.value) document.addEventListener("keyup", enableAuto);

		return;
	}

});

chrome.storage.sync.get(["excluded_urls", "auto_correction"]).then((result) => {
	document.removeEventListener("keyup", enableAuto);
	if(
		result.auto_correction === true && 
		!result.excluded_urls[activeTabUrl]
	) {
		document.addEventListener("keyup", enableAuto);
	} 

});
